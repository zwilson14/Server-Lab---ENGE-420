//==============================================================================
void buttons_init()
{
  // Enables for signals
  PORT->Group[BTN_0_GROUP].PINCFG[BTN_0_PIN].bit.INEN = 1;
  
  // Config as inputs by clearing the register
  PORT->Group[BTN_0_GROUP].DIRCLR.reg = BTN_0;
  
}

// This goes off when GS (BTN_ACTIVE) goes off
uint8_t button_read()
{
  //Clear the value first:
  button_val = 1;  
  
  if (BTN_0_VAL)
  {
    button_val = 0;
  }
  
  return button_val;
}

