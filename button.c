// Button Definitions:
//  BTN          ARM
// ==================
//  BTN_ACTIVE   PA08
//  BTN_0	      PB09
//  BTN_1        PA04
#define BTN_0 (PORT_PB23)
#define BTN_0_GROUP (1)
#define BTN_0_PIN (PIN_PB23%32)

// IN REGs
#define BTN_0_VAL ((PORT->Group[BTN_0_GROUP].IN.reg) & BTN_0)


// Actual Button representation values:
static uint8_t button_val = 0;

